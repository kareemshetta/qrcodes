  async function generateQRCodes(data) {
    // Create base directory for QR codes if it doesn't exist
    const baseDir = path.join(__dirname, 'qrcodes-v2');
    if (!fs.existsSync(baseDir)) {
        fs.mkdirSync(baseDir);
    }

    for (const item of data) {
        // Create directory for each ID
        const dirPath = path.join(baseDir, item.name);
        if (!fs.existsSync(dirPath)) {
            fs.mkdirSync(dirPath);
        }

        // Generate QR code image
        const filePath = path.join(dirPath, `${item.name}.png`);
        try {
            await qrcode.toFile(filePath, item.qrcodeUrl, {
                width: 300,
                margin: 2,
                color: {
                    dark: '#000000',
                    light: '#ffffff'
                }
            });
            console.log(`Generated QR code for ID: ${item.id}`);
        } catch (err) {
            console.error(`Error generating QR code for ID: ${item.id}`, err);
        }
    }
}

